    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        // TODO add your handling code here:
        
        if (cmbClientes.getSelectedIndex() == -1) {
        JOptionPane.showMessageDialog(
                this,
                "No hay clientes disponibles. Registre un cliente primero.",
                "Sin clientes",
                JOptionPane.WARNING_MESSAGE
        );
        return;
    }
        
        if (cmbHabitaciones.getSelectedIndex() == -1) {
        JOptionPane.showMessageDialog(
                this,
                "No hay habitaciones disponibles.",
                "Sin habitaciones",
                JOptionPane.WARNING_MESSAGE
        );
        return;
    }
        
        
        String textoEntrada = txtFechaEntrada.getText().trim();
        String textoSalida = txtFechaSalida.getText().trim();
         if (textoEntrada.isEmpty() || textoSalida.isEmpty()) {
        JOptionPane.showMessageDialog(
                this,
                "Debe ingresar la fecha de entrada y la fecha de salida.",
                "Fechas incompletas",
                JOptionPane.ERROR_MESSAGE
        );
        return;
    }
         
         SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            formato.setLenient(false);
         
          try {
        Date fechaEntrada = formato.parse(textoEntrada);
        Date fechaSalida = formato.parse(textoSalida);

        int indiceCliente = cmbClientes.getSelectedIndex();
        int indiceHabitacion = cmbHabitaciones.getSelectedIndex();

        Cliente clienteSeleccionado =
                hotel.getClientes().get(indiceCliente);

        Habitacion habitacionSeleccionada =
                hotel.buscarHabitacionesDisponibles().get(indiceHabitacion);

        String idReserva = "RES-" + (hotel.getReservas().size() + 1);

        Reserva nuevaReserva = new Reserva(
                idReserva,
                clienteSeleccionado,
                habitacionSeleccionada,
                fechaEntrada,
                fechaSalida
        );

        hotel.registrarReserva(nuevaReserva);

        Factura factura = new Factura(
                "FAC-" + String.format("%03d", hotel.getReservas().size()),
                nuevaReserva
        );

        JOptionPane.showMessageDialog(
                this,
                "Reserva registrada correctamente.\n\n"
                + "ID de reserva: " + idReserva + "\n"
                + "Cliente: " + clienteSeleccionado.getNombre()
                + " " + clienteSeleccionado.getApellido() + "\n"
                + "Habitación: " + habitacionSeleccionada.getNumero() + "\n"
                + "Total: $" + String.format("%.2f", nuevaReserva.calcularTotal()),
                "Reserva exitosa",
                JOptionPane.INFORMATION_MESSAGE
        );

        txtFechaEntrada.setText("");
        txtFechaSalida.setText("");

        cargarHabitaciones();

    } catch (ParseException e) {
        JOptionPane.showMessageDialog(
                this,
                "Las fechas deben tener el formato AAAA-MM-DD.\n"
                + "Ejemplo: 2026-07-20",
                "Formato de fecha incorrecto",
                JOptionPane.ERROR_MESSAGE
        );

    } catch (Exception e) {
        JOptionPane.showMessageDialog(
                this,
                e.getMessage(),
                "No se pudo registrar la reserva",
                JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_btnReservarActionPerformed

