    private void cargarClientes(){
        cmbClientes.removeAllItems();
        for (Cliente cliente : hotel.getClientes()){
            cmbClientes.addItem(cliente.getNombre() + " " + cliente.getApellido() + " - " + cliente.getCedula());
        }
    }
    
    private void cargarHabitaciones() {
    cmbHabitaciones.removeAllItems();

    for (Habitacion habitacion
            : hotel.buscarHabitacionesDisponibles()) {

        cmbHabitaciones.addItem(
                habitacion.getNumero()
                + " - "
                + habitacion.getClass().getSimpleName()
                + " - $"
                + String.format("%.2f", habitacion.calcularPrecio())
        );
    }
}
    
    
    
